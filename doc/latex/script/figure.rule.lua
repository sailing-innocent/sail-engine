rule("latex.direct_image")
    set_extensions(".png", ".jpg", ".jpeg", ".pdf", ".eps")
    on_load(function (target)
        target:set("targetdir", path.join("build", "asset"))
        os.mkdir(target:targetdir())
        target:set("kind", "object")
    end)
    on_build_file(function (target, sourcefile, opt)
        import("utils.progress")
        os.cp(sourcefile, target:targetdir())
        -- progress.show(opt.progress, "moving %s", sourcefile)
    end)
rule_end()

rule("latex.figure")
    add_deps(
        "latex.graphviz", 
        "latex.direct_image", 
        "latex.python_result", 
        "latex.ppm_image")
rule_end()

function add_img(name, ext)
    ext = ext or "png"
    target(name)
        add_rules("latex.figure")
        add_files(name.."."..ext)
    target_end()
end
